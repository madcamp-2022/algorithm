n = int(input())
m = int(input())

if (n==1) or (n==5):
    print((n-1) + (8*m))
else:
    # m==0일 때도, m%2==0으로 분기처리
    if (m%2==0):
        print((n-1) + (8*int(m/2)))
    else:
        print((9-n) + (8*int(m/2)))

